package tasks.homework.day2.basetask;

public class TrainMethodsPrimitive {

    public void println(int i) {

        System.out.println("Я получил на вход число " + i);
    }

    public void printLong(long i) {
        System.out.println("Я получил на вход длинное число " + i);

    }

    public void printChar(char ch) {
        System.out.println("Я получил на вход символ " + ch);
    }

    public void printFloat(float f) {
        System.out.println("Я получил на вход дробное число " + f);
    }

    public void printDouble(double d) {
        System.out.println("Я получил на вход длинное дробное число " + d);
    }

    public void printShort(short sh) {
        System.out.println("Я получил на вход короткое число " + sh);
    }

    public void printByte(byte b) {
        System.out.println("Я получил на вход очень коротное число " + b);
    }

    public void printBoolean(boolean b) {
        System.out.println("Я получил на вход булево " + b);
    }

}
